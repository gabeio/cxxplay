#include <iostream>

int main()
{
    bool a = 0;
    bool b = 1;
    bool c = -1;
    bool d = 100;
    bool e = -100;
    std::cout << "a=0==" << a << std::endl;
    std::cout << "b=1==" << b << std::endl;
    std::cout << "c=-1==" << c << std::endl;
    std::cout << "d=100==" << d << std::endl;
    std::cout << "e=-100==" << e << std::endl;
    
    std::cout << std::endl; //blank line
    
    return 0;
}